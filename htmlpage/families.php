    <h2>Life on Rocky Island in Early 20th Century - Families</h2>
    <p>Some of the other families who lived on Rocky Island, in addition to Nancy’s family the Elders, were:
      <hr>
      <strong>Mrs Foster:</strong> Her father, Mr Douglas, was a local builder and handyman. Her story was featured in
      the local press in 1961 (see left) when, as one of the last residents of Rocky Island, she refused to leave when
      Whitley Bay Council tried to ‘force’ her out.
      <hr>
      <strong>Mrs. Taylor:</strong> Her son was a baker, and she would serve tea and cakes to visitors, who would often
      queue down the garden path!
      <hr>
      <strong>Mrs. Heads and Miss Dorrie Park:</strong> Both these ladies sold hot water and tea from trays in their
      gardens. Miss Park was also a dressmaker, who made clothes for the children of the island, often made from worn
      out adult clothing. This was in addition to one of her other important duties, to carry a shovel of hot coals up
      to the Seaton Sluice Volunteer Life Saving Company (SSVLSC) Watch House to start the fire there whenever there was
      a meeting or a social!
      <hr>
      <strong>Mr. Jimmy Dickinson:</strong> a joiner.
      <hr>
      <strong>Mrs Watson:</strong> Born on Rocky Island, returned in 1935, aged 101, to revisit her birthplace (see
      below)
      <hr>
      <strong>Mr. Marley:</strong> He owned a horse and cart, selling hardware, and what were referred to as “piss pots”
      hanging around the cart, and he stabled his horse and stored his goods, including china etc. at his two storey
      house. His horse, Barney, would drink from the island’s communal tap, waiting around the tap for someone passing
      to stop and turn it on for him!
      <hr>
      <strong>Mr T Ions:</strong> Thomas Ions’ house was the biggest on the island, and he was an accomplished pianist
      who had lived in Canada for a number of years. He was the son of Thomas and Hager Ions (both pictured below), who
      lived on the Island, and he continued to live there on his return from Canada, after his parents died.
      <hr>
      Hager Ions was a Durrant before she was married, and she lived on the Island in 1861.
      <hr>
      The house they lived in was double fronted (see top-right picture), with the staircase going up the middle of the
      house.
      <hr>
      Hager used to send Nancy to the co-op for a hapeth of yeast, and give her a penny for going. When they died (all
      three of them), it was Nancy's mother who laid them out. All three are buried in the Parish Church of Our Lady,
      Delaval.
      <hr>
      Nancy remembered Hager's daughter, Mary Salmon (nee Ions), who used to come once a year for her holidays with her
      children. She was known to her and others as, 'Granny Salmon.' She used to bring a big bag of sweets with her to
      give to all of the children on the Island. When Thomas, the son, lived there, he had a beautiful garden. He used
      to take a bowl with rice, milk and sugar in it to Nancy's mum and say to her, “Sarah, will you put this in the
      oven and cook it for me please?” - apparently he didn't like spending money!
      <hr>
      Thomas was the last Ions to live on Rocky Island. When he died, in January 1947, the house was empty for a while,
      then other families came and lived there.
      <hr>
      Thanks to Jane Squires (great, great, granddaughter to Hager and Thomas Ions), who supplied the Ions’ family
      information and photographs.
