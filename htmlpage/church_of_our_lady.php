      <h2>Church of our Lady</h2>
      <p>The Church of Our Lady, Seaton, is the Parish Church of the Parish of Delaval. The Church was built about the
        end of the 11th Century by Guy de Laval, the nephew, by marriage, of William the Conqueror, and consecrated in
        1102 by Bishop Flambard of Durham.
        <hr>
        The Nave is separate from the Chancel by two fine Norman arches, which is quite rare in such a small building. A
        blocked Saxon window, visible on the outside of the North wall of the Nave, suggests an even earlier building.
        On the Nave walls and the West wall hang six funeral Hatchments (Coats of Arms), of which there are only 50 in
        the whole of Northumberland.
        <hr>
        For over 700 years the Church remained the private chapel of the Delaval family and their retainers. On the
        death, in 1814, of the last male Delaval the estate passed, by marriage, to a Norfolk family, the descendants of
        Sir Thomas de Astley. Sir Jacob Henry Astley of Kings Lynn, inherited the property. In 1817 it passed to his
        son, Jacob, who revived the family title of Baron Hastings in 1841. In 1891 the grandfather of the late Lord
        Hastings presented the building to the Church of England for ever as the Parish Church of the Parish of Delaval.
        <hr>
