  <p>Extracted from "<strong>Life on Rocky Island, Seaton Sluice, As remembered by Nancy Davison</strong>", compiled by Hugh and Helen Lamont.</p>
  <h2>Rocky Island is Created</h2>
  <p>It’s difficult to imagine nowadays what a busy and thriving port Seaton Sluice was between the 17th and 19th centuries, with the harbour being the centre of activity for 
    exports of glass from the Royal Hartley Bottle Works (see below), coal, and shipbuilding.
    <hr>
    Despite various alterations to the harbour in 1690, the old basin was no longer able to cope with the increasing volume of trade. In 1761 therefore, Sir John Hussey Delaval, 
    and his brother Thomas, commissioned work on a new harbour, and it was eventually opened on March 20 1764, at a cost of £10,000.
    <hr>   
    This involved making a cut, eastward through the solid rock of the old harbour, with gates at both ends, thus providing the harbour with an additional entrance, and at the 
    same time forming a deep water dock, where vessels could be loaded by spouts or cranes at any state of the tide. The original entrance to the harbour was used in stormy 
    weather, and the new cut was used when the weather was moderate.
    <hr>    
    Until the establishment of “The Cut” in 1764, Rocky Island was an integral part of the mainland of Seaton Sluice, adjacent to the site of The Kings Arms, before becoming a 
    “proper” island (except at low tide) when “The Cut” was formed.
    <hr>    
    On the ‘other side’ of the harbour, lies Sandy Island, which is not actually an island at all, but is now equally depopulated, having once been, like Rocky Island, a separate, 
    thriving little community. The most prominent feature of modern day Sandy Island is the Ballast Hill, formed with ballast unloaded from ships returning from delivering coal, 
    bottles, copperas, etc., to London and other Southern ports.
    <hr>    
    The trading port of Seaton Sluice exported coal (loaded onto ships from staithes and coal chutes down the side of the “cut”), and bottles from the Royal Hartley Bottle Works 
    (located near to St Paul’s Church, and Beresford Court – where the fish & chip shop is currently situated), along with salt and copperas.
    <hr>    
    However, the salt and copperas industries had closed by 1828 and, following the improvements carried out at Blyth and at Northumberland Dock on the Tyne in 1848, the coal trade also closed in 1862 (following the Hartley Pit disaster). The last bottles left the Royal Bottle Works in 1872 on the 'Unity of Boston' bound for the Channel Islands, and Seaton Sluice harbour became the tranquil place that we know today.
    </p>
