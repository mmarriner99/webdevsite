  <h2>Shipping</h2>
  <p>With the increase in trade over the years at Seaton Sluice, and despite various alterations to the harbour in 1690, plans were drawn up for a new harbour or dock, due to the 
    old basin being inadequate to cope with the trade of the port. Work on the new harbour started in 1761, and it was eventually opened on March 20 1764, at a cost of £10,000. A 
    cut was made eastward through the solid rock of the old harbour, with sluice gates at both ends, thus providing the harbour with an additional entrance, and at the same time 
    forming a deep water dock, where vessels could be loaded by spouts or cranes at any state of the tide.
    <hr>
    It is from the construction of the new harbour, with its new sluice gates that gave rise to the name of the village as Seaton Sluice. The original entrance to the harbour was 
    used in stormy weather, and the new cut or south entrance was used when the weather was moderate. Both entrances had piers which extended some distance into the sea, and on the 
    approach of any ship a flag was hoisted by the pilots directing the vessel to the proper entrance, according to the state of the weather, and at night-time a lamp of burning 
    coal was hung out as a signal.
    <hr> 
    Because there were then no steam tugs to assist in towing and mooring, a line would be thrown on board a ship, and a number of willing men would haul the vessel to its moorings
    in the harbour, for which duty they would be given a ticket entitling them to a quart of beer each at one of the many local ale houses.
    <hr>
    The harbour mouth was protected by booms, and every ship which entered had to pay five shillings for the lifting of these booms. A drawbridge was built across the cutting, 
    which was later replaced with a bridge, which still stands to this day. Forty ships, of two and three hundred tons burden, were regularly engaged in the carriage of passengers 
    and merchandise to London and many other foreign ports, and in 1785, the taxes paid to the Government on salt, glass, and coal by this little seaport alone amounted to a 
    staggering £24,000. There was a downside to the increase in shipping however. A glance at any shipwreck map of England will highlight the predominance of wrecks along the 
    Northumbrian coast. It was not uncommon at this time for crews and passengers to be drowned only a stone's throw from the shore whilst crowds watched on helplessly. This led to
    Seaton Sluice Volunteer Life Saving Company being formed.
