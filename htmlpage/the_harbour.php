      <h2>The Harbour</h2>
      <p>From a natural rill at the beginning of the 17th century, the harbour of Seaton Sluice was transformed by Sir
        Ralph Delaval between 1660 and l690 to satisfy an
        increasing demand for Hartley coal. Stone walls were raised, piers were built at the north entrance to the
        harbour and an ingenious system of sluice gates installed,
        which enabled the tidal waves to scour the bottom of the channel to remove sand and silt which continually built
        up in the harbour entrance. These early improvements
        were not entirely successful due to the limited depth of water in the natural harbour and ships had to be
        part-loaded then taken out into deeper water at the entrance of the
        harbour to be loaded to their full capacity by keel boats, adding both to the expense and causing delays. Access
        to the harbour was also extremely difficult when a
        North-East wind prevailed and the limitations of these early improvements to the harbour, in coping with the
        ever-increasing trade in coal arid salt, were soon realised.
        <hr>
        By the middle of the 18th century Sir John Hussey Delaval in conjunction with his brother, Thomas Delaval, had
        drawn up plans and specifications to cut a new harbour eastward
        through solid rock. Tenders were invited in August 1761 in the Newcastle Courant and by 1764 the work was
        completed. A major engineering feat in its day the 'cut, or 'gut'
        as it is now locally known, was some 270 metres long. 9 metres wide, 15 metres deep. On the 22 August 1764 with
        Captain Curry at the helm, the 'Warkworth' was the first to
        sail out of the new harbour with a cargo of 270 tonne of coal.
      </p>
