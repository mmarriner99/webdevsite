      <div class="person">
        <span class="name">Thomas de Astley</span>
        <span class="dates">1153-1220</span>
        <p>He was Bailiff and cousin to Simon de Montfort. He was at Runnymede in 1215 when King John signed Magna Carta.</p>
      </div>
      <div class="person">
        <span class="name">Thomas de Astley</span>
        <span class="dates">1215-1265</span>
        <p>He married twice. In 1236 his second wife was Edith, daughter of Peter Constable of Melton Constable. Edith inherited Melton Constable, and other lordships in Norfolk. Edith was the founder of the branch of the family which have possesses Melton Constable, in unbroken male descent, from 1236.</p>
      </div>
      <div class="person">
        <span class="name">Ralph Astley</span>
        <span class="dates">Died 1370</span>
        <p>He fought in the wars of Edward III, and was at the Battle of Crecy (1346) with the Black Prince. He was Knighted there, and permitted to bear five white feathers at his crest.</p>
      </div>
      <div class="person">
        <span class="name">Thomas Astley</span>
        <span class="dates">Died 1545</span>
        <p>He built a new house at Melton Constable. It was built around a courtyard without fortifications.</p>
      </div>
      <div class="person">
        <span class="name">John Astley</span>
        <span class="dates">Died 1596</span>
        <p>He was the first cousin of Anne Boleyn. He married Catherine, daughter of Sir Philip Champernowne of Bere and Modbury, in Devon. She is better known as Mistress Astley, who brought up Queen Elizabeth</p>
      </div>
      <div class="person">
        <span class="name">Sir Jacob Astley</span>
        <span class="dates">1579-1651</span>
        <p>Fought in the Netherlands in the Thirty years War. In the Civil War, in 1642, he fought on the Royalist side and, at the Battle of Nasby, he commanded the main body of the foot. It was he who said, before the Battle of Edgehill, “Oh lord thou knowest how busy I must be this day. If I forget thee, do not thou forget me”.</p>
      </div>
      <div class="person">
        <span class="name">Jacob Astley</span>
        <span class="dates">1640-1729</span>
        <p>The family were strong supporters of the monarchy. At 13, in 11653, Jacob inherited a looted and wrecked estate, so he joined the future Charles II in exile on the continent. At the restoration, he was knighted and created a baronet. A grateful Charles II gave him a marble bust of himself, and a painting of Windsor Castle by Vastermans. Jacob set about restoring his estate, and rebuilding the family mansion at melton Constable. The new house was probably designed by Sir Christopher Wren, and completed in 1690. He died at the age of 90.</p>
      </div>
      <div class="person">
        <span class="name">Sir Edward Astley</span>
        <span class="dates">1729-1802</span>
        <p>He married Rhoda Delaval in 1751 and so joined the two families. His son Jacob inherited the Astley Estate on his death in 1802, and the Delaval Estate on the death of Edward Delaval in 1814.</p>
      </div>
      <div class="person">
        <span class="name">Sir Jacob Astley</span>
        <span class="dates">1797-1859</span>
        <p>He lived at Melton Constable, and he left Delaval Hall in the care of servants. In 1822, the centre block of Delaval Hall was badly damaged by fire. Sir Jacob neglected Delaval Hall, and it was not until 1860 that essential repairs were carried out. It was Sir Jacob who revived the Hastings title, and he was named 16th Baron Hastings in 1841, the title having been in abeyance since the 15th century.</p>
      </div>
      <div class="person">
        <span class="name">Sir George Astley</span>
        <span class="dates">1857-1904</span>
        <p>He gave the Church of Our Lady to the newly formed parish of delaval in 1891. He also gave the burial ground, vicarage, and an endowment for the living.</p>
