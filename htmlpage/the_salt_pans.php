      <h2>The Salt Pans</h2>
      <p>Hartley Pans was the old name for Seaton Sluice, a name derived from the staple industry of salt making. Salt
        was produced at the mouth of the Seaton Burn as far back as 1236; sea water was evaporated in huge pans, heated
        by coal fires. The coal was gathered, open-cast fashion, between the Crag Point and Bates Hill, although it
        appears that in later years mined coal was used. At this time, the salt was dried and then carried in wains to
        Blyth to be transported to other parts of the country, and it wasn't until 1550 that salt was shipped from
        Hartley Haven. This chiefly went to Yarmouth and Hull, where it was used in the curing of herrings.
        <hr>
        During Queen Elizabeth's reign there were eight pans, each producing two tons of salt a week; the market price
        was thirty shillings a ton. Sir John Delaval's pans were mentioned in a book written by Dr. Wm. Bullien,
        published in London in 1564, and from another journal of that period, we learn that Hartley salt was "esteemed
        by sutche as buye same to be better than any other white salt".
        <hr>
        The salt trade continued to flourish for many years, and it wasn't until 1782 that real trouble came to the
        industry. A tax was levied at sevenpence a bushel, and officers of the excise were placed at Flodden to stop
        salt going over the border into Scotland. This did the salt trade a lot of harm, and was the beginning for a
        black-market in salt.
        <hr>
        Many tales are told of local smuggling, how the good wives of Hartley Pans contrived to smuggle half a stone of
        salt into Newcastle and provided they were successful, they could trade the salt for enough groceries to last a
        week. The year 1798 saw the end of the shipping of white salt from Hartley. The Government issued an order in
        which they forbade the making of white salt and foul salt (alkali) in the same place or pans. The pans at
        Hartley were too old to change over to new methods, so the marketing of white salt came to an end.Salt continued
        to be manufactured for a few more years, but this was all used in the Glass Houses. The pans were fully closed
        down in 1820, and no trace of them now remain. They were situated on the right bank of the river, east of the
        main road bridge, and the salt drying houses were on Rocky Island, which at that time was known as the "Pans
        Close".
      </p>
