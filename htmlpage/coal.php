      <h2>Coal</h2>
      <p>Coal was brought to the harbour from the coalfields south and west of the village on wagonways which traversed
        the area in all directions. Ships were loaded from staithes
        in front of the Kings Arms and from coal chutes down the side of the new 'cut'. Lighter bottle ships were loaded
        in the shallowest part of the harbour from the quay below the
        present concrete bridge. With these two entrances to the harbour and piers both sides extending out to sea, the
        'cut' was a success and in 1777 one hundred and seventy seven
        ships sailed out of the harbour with a total 48,000 tonnes of coal; the 'cut' could also be sealed off at both
        ends at high tide to serve as a dock to allow loading to continue
        at all times of the tide and day.
        <hr>
        Coal continued to be shipped from the port but the Salt Laws of 1797, and a lack of orders for copperas had
        closed both these industries by 1828. A cry for better shipping
        facilities by the colliery owners, in 1848. finally led to major improvements to Blyth and Northumberland Dock
        on the Tyne, which had been funded by corporate bodies such as
        the River Commissioners rather than one individual family as at Seaton Sluice. The coal trade was finally ruined
        in 1862 following the Hartley (Hester) Pit disaster. The Bottle
        works closed in 1870 and Seaton Sluice went into decline.
