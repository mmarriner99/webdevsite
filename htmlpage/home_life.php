      <h2>Life on Rocky Island in Early 20th Century - Home Life</h2>
      <p>Toilet facilities were outside and, with no mains drainage, had to be emptied each week. This involved local
        workers bringing a wagon to the end of the bridge (outside The Kings Arms) and then carrying the waste buckets
        from the toilets on the island to be emptied into the wagon. Nancy’s mother, along with the other householders,
        would put hot ashes from the fire into the toilet buckets to absorb the smells.
        <hr>
      </p>
      <p>There were no shops on the island, but some of the residents, including Nancy’s grandmother, would sell various
        home-made goods from their homes. Nancy’s own grandmother sold home-made toffee and bread, and many other
        residents shared this entrepreneurial spirit.
        <hr>
      </p>
      <p>Nancy also recalls that health care was provided by everyone on the island making regular payments into a fund
        to pay for the services of a doctor and nurse, and of the children being despatched for “cough bottles” from the
        doctor, and having a sly taste on the way home!
        <hr>
      </p>
      <p>When the greengrocer visited, or paraffin oil was delivered for the lights, the residents had to cross the
        bridge “to the mainland” to collect their provisions, although fresh milk was delivered to their doors twice
        daily from the Lodge Farm (there were no fridges in those days). Paraffin oil for the lights was important, as
        there was no electricity on the island, and gas lighting was only introduced in the late 1930s.
        <hr>
      </p>
      <p>The majority of men on the island earned their living locally as coal miners, and as such, were entitled to
        free coal. This coal was dumped outside the Kings Arms for them to collect, and all the residents worked
        together to bring it across to the island. Nancy recalls that no-one had a wheelbarrow, so it had to be carried
        in anything that was available, including the tin baths! Pity the poor person who had the first bath after the
        coal delivery.Nancy used to love Fridays, because that was the day that her mother would fill the boiler in the
        wash house so that they could have hot water for a bath.
        <hr>
      </p>
